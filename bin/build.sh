#!/bin/bash
# ============================================================================
# DEVELOPMENT SCRIPT - Not for production use or users
# ============================================================================
# Purpose: Local testing of pip build process during migration
# 
# This script:
#   - Creates virtual environments
#   - Builds the package (wheel + tarball)
#   - Tests installation in an isolated temp directory
#
# Usage: ./bin/build.sh
#
# Note: This script is part of the git history to document the migration
# process. It is not intended to be run by users or as part of CI/CD.
# ============================================================================

set -e

# project details
PROJECT="word-count-reporter"
# project with - chars converted to _ (PyPI normalizes - to _ when building; also needed for import)
PROJECT_NORMALIZED="${PROJECT//-/_}"
VERSION="1.0.0"

# working directories

# Absolute path to this script's directory
SCRIPT_DIR="$(realpath "$(dirname "$0")")"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
# dir to build from (dir with pyproject.toml)
BUILD_DIR="${REPO_ROOT}"
# dist dir that should be generated by build
DIST_DIR="${BUILD_DIR}/dist"

# wheel file details
WHEEL_NAME="${PROJECT_NORMALIZED}-${VERSION}-py3-none-any.whl"
WHEEL_FILE="${DIST_DIR}/${WHEEL_NAME}"
TARFILE_NAME="${PROJECT_NORMALIZED}-${VERSION}.tar.gz"

# test md files
TEST_FILES="${REPO_ROOT}/example_files"

# virtual envs to use
dev_venv="dev_env"
test_venv="test_env"
requirements_file="${REPO_ROOT}/requirements-dev.txt"

echo "-----------------------------"
echo "| Script dir      : ${SCRIPT_DIR}"
echo "| Repo root       : ${REPO_ROOT}"
echo "| dev requirements: ${requirements_file}"
echo "| Build dir       : ${BUILD_DIR}"
echo "| Dist dir        : ${DIST_DIR}"
echo "| Wheel path      :${WHEEL_FILE}"
echo "-----------------------------"

# Set up a Python virtual environment with requirements
#
# Creates a new venv if it doesn't exist, or reuses an existing one.
# Always upgrades pip and installs runtime requirements.
#
# Args:
#   $1 - venv_path: Path to virtual environment directory (e.g., "dev-env")
#   $2 - requirements_file: Path to requirements.txt file
#
# Usage:
#   setup_venv "dev-env" "requirements.txt"
setup_venv() {
    local venv="$1"
    local requirements="$2"
    
    if [[ ! -d "${venv}" ]]; then
        echo "Creating new virtual environment..."
        virtualenv "${venv}"
    fi
    
    # Activate and install (common for both create and reactivate)
    echo "Activating virtual environment ${venv}..."
    source "./${venv}/Scripts/activate"
    
    # Upgrade pip
    # Use python -m pip - works on all platforms and prevents pip self-modification issues
    python -m pip install --upgrade pip
    
    # Install runtime requirements if changed
    if [[ -n "${requirements}" && -f "${requirements}" && "${requirements}" -nt "${venv}/.requirements_installed" ]]; then
        echo "Installing runtime requirements..."
        echo "python -m pip install -r ${requirements}"
        python -m pip install -r "${requirements}"
    fi
    
    # Mark as installed
    touch "${venv}/.requirements_installed"

    echo "Virtual environment ready: ${venv}"
}

# Check a file exists and fail if it doesn't
check_file() {
    local filepath="$1"

    if [[ -f "${filepath}" ]]; then
        echo "${filepath} exists"
    else
        echo "${filepath} does not exist! Exiting."
        exit 1
    fi
}

# Check that both wheel and tarball distribution files exist
#
# Args:
#   $1 - dist_dir: Path to directory containing built distributions (e.g., "dist")
#   $2 - version: Package version string (e.g., "0.1.0")
#
# Returns:
#   0 if both files exist, 1 otherwise
#
# Usage:
#   if check_dist_files "dist" "0.1.0"; then
#       echo "Ready to upload"
#   fi
check_dist_files() {
    local dist_dir="$1"
    local version="$2"  # e.g., "0.1.0"
    
    local wheel_file="${dist_dir}/${WHEEL_NAME}"
    local tarball_file="${dist_dir}/${TARFILE_NAME}"
    local missing_files=()
    
    if [[ ! -f "${wheel_file}" ]]; then
        missing_files+=("$(basename "${wheel_file}")")
    fi
    
    if [[ ! -f "${tarball_file}" ]]; then
        missing_files+=("$(basename "${tarball_file}")")
    fi
    
    if [[ ${#missing_files[@]} -gt 0 ]]; then
        echo "❌ Missing distribution files:"
        printf '   - %s\n' "${missing_files[@]}"
        return 1
    fi
    
    echo "✅ Distribution files found:"
    echo "   - $(basename "${wheel_file}")"
    echo "   - $(basename "${tarball_file}")"
    return 0
}

# Create a temporary directory for isolated testing
# 
# Directory location:
#   Uses Windows %TEMP% environment variable (typically C:\Users\<User>\AppData\Local\Temp)
#   Falls back to %TMP% if TEMP is not set
#   Falls back to C:\temp if neither is set
#
# Directory name format: word-count-reporter-test-<PID>-<RANDOM>
#   - PID: Current shell process ID (ensures uniqueness across processes)
#   - RANDOM: Bash built-in random number (0-32767) for extra uniqueness
#
# Full path example:
#   C:\Users\YourName\AppData\Local\Temp\word-count-reporter-test-12345-1234
#
# Returns:
#   Echoes the absolute path to the created temporary directory
#
# Example:
#   TEMP_DIR=$(create_temp_dir)
#   cd "${TEMP_DIR}"
#   # ... run tests ...
#   rm -rf "${TEMP_DIR}"
#
# Notes:
#   - Caller is responsible for deleting the directory when done
#   - Directory is created with mkdir -p (creates parent dirs if needed)
create_temp_dir() {
    local base="${TEMP:-${TMP:-C:\\temp}}"
    base="${base//\\//}"  # Convert backslashes to forward slashes for Bash
    local dirname="${base}/${PROJECT}-test-$$-${RANDOM}"
    mkdir -p "${dirname}"
    echo "${dirname}"
}

cleanup() {
    local testing_dir="$1"

    if [[ -n "${testing_dir}" && -d "${testing_dir}" ]]; then
        echo "Test directory: ${testing_dir}"
        echo "Remove with: rm -rf \"${testing_dir}\""
        # rm -rf "${testing_dir}"  # Uncomment after verifying
    fi
}

# 0. cd to dir to build from
echo ""
echo "##########################################################"
echo "## 0. cd to the dedicated build dir                      #"
echo "##########################################################"
echo ""
mkdir -p "${BUILD_DIR}"
cd "${BUILD_DIR}"
echo "Building from: $(pwd)"

# 1. Set up development virtual environment and install build tools
echo ""
echo "##########################################################"
echo "## 1. Setup development env and install build tools      #"
echo "##########################################################"
echo ""
check_file "${requirements_file}"
setup_venv "${dev_venv}" "${requirements_file}"

# 2. Clean any previous builds (important!)
echo ""
echo "##########################################################"
echo "## 2. Clean any previous builds (important!)             #"
echo "##########################################################"
echo ""
rm -rf dist/ build/ *.egg-info

# 3. Build your package
echo ""
echo "##########################################################"
echo "## 3. Build package with pythom -m build                 #"
echo "##########################################################"
echo ""
python -m build

# 4. Check build files generated
echo ""
echo "##########################################################"
echo "## 4. Check build files generated                        #"
echo "##########################################################"
echo ""
if check_dist_files "${DIST_DIR}" "${VERSION}"; then
    echo "Ready to upload to PyPI testing"
    # proceed with twine upload
else
    echo "Build failed or incomplete"
    exit 1
fi

# 5. Create a temp directory for testing
echo ""
echo "##########################################################"
echo "## 5. Create a temp dir for testing and cd to it         #"
echo "##########################################################"
echo ""
# Create temp directory (still in project dir)
TEMP_DIR=$(create_temp_dir)

# Now cd out of project and into temp dir
cd "${TEMP_DIR}" || exit 1

# We are now completely outside the project
echo "Testing in: $(pwd)"

echo ""
echo "##########################################################"
echo "## 6. Setup test virtualenv from tempdir                 #"
echo "##########################################################"
echo ""
setup_venv "${test_venv}"

# Now safe to test
echo ""
echo "##########################################################"
echo "## 7. Install wheel file from build                      #"
echo "##########################################################"
echo ""
python -m pip install "${WHEEL_FILE}"

# Test the installed package
echo ""
echo "##########################################################"
echo "## 8. Test package import                                #"
echo "##########################################################"
echo ""
echo "Testing package import..."
if python -c "import ${PROJECT_NORMALIZED}; print(f'Version: {${PROJECT_NORMALIZED}.__version__}')" 2>/dev/null; then
    echo "✅ Package import successful"
else
    echo "❌ Package import failed - check installation"
    exit 1
fi

examples_dirname=$(basename "$TEST_FILES")
echo ""
echo "##########################################################"
echo "## 9. Copy example markdown files into testing dir"
echo "##########################################################"
echo ""
# copy example files into testing dir
cp -r "${TEST_FILES}" "${TEMP_DIR}/${examples_dirname}"

# After all testing is done, output the activation command
echo ""
echo "═══════════════════════════════════════════════════════════"
echo "                    BUILD COMPLETE ✅"
echo "═══════════════════════════════════════════════════════════"
echo ""
echo "📁 Test dir:  ${TEMP_DIR}"
echo ""
echo "🔧 Activate:  cd ${TEMP_DIR} && source ${test_venv}/Scripts/activate"
echo "🏃 Run:       ${PROJECT} ${examples_dirname}"
echo "═══════════════════════════════════════════════════════════"
echo ""

